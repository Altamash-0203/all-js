//postfix increment
let y = 5;
let x = y++; //y=6 &x =5

console.log(y)
console.log(x)



//prefix increment
let m= 5;
let n = ++m; //both is 6

console.log(m)
console.log(n)

//postfix decrement
let h= 5;
let i = h--;   /// i=5&h=4

console.log(h)
console.log(i)

//prefix decrement
let k= 5;
let l = --k;   /// k=4&l=$

console.log(k)
console.log(l)